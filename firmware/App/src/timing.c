
/*============================================ Includes ==========================================*/

#include "timing.h"

/*========================================= Private Data =========================================*/

static volatile uint8_t sysTick_flag = 0U;

/*======================================= Public Functions =======================================*/

/***************************************************************************************************
* x
***************************************************************************************************/
void timing_sysTickISR(void)
{
	sysTick_flag = 1U;
}

/***************************************************************************************************
* x
***************************************************************************************************/
void timing_waitForSysTick(void)
{
	while(sysTick_flag == 0U)
	{
		; // wait for interrupt.
	}
	sysTick_flag = 0U;
}
